/**
 * 1. bigger: > (greater than)
 * 2. less: < (less than)
 * 3. equal: ==, ===
 * 4. Greater than or equal: >=
 * 5. Less than or equal: <=
 * 6. Not equal: !=, !==
 * 7. and: &&
 * 8. or ||
 * 
 * 
 */

const a = 10;
const b = 7;
console.log(a > b);
console.log(a < b);
console.log(a === b);
console.log(a >= b);
console.log(a <= b);
console.log(a !== b);